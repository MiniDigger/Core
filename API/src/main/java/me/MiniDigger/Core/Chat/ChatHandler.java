package me.MiniDigger.Core.Chat;

import me.MiniDigger.Core.User.User;

public interface ChatHandler {
	
	/**
	 * Returns the channel with this name
	 * 
	 * @param name
	 *            The name
	 * @return The channel if found, else NULL
	 */
	public ChatChannel getChannel(String name);
	
	/**
	 * Register a channel
	 * 
	 * @param channel
	 *            The channel to register
	 */
	public void registerChannel(ChatChannel channel);
	
	/**
	 * Unregisters a channel
	 * 
	 * @param channel
	 *            Teh channel to unregister
	 */
	public void unregisterChannel(ChatChannel channel);
	
	/**
	 * Registers all channnels
	 */
	public void registerChannels();
	
	/**
	 * Handles a chat message send by an user
	 * 
	 * @param user
	 *            The user who send the message
	 * @param message
	 *            The message send
	 */
	public void handleChat(User user, String message);
}
