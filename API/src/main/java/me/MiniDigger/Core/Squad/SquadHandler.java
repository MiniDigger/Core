package me.MiniDigger.Core.Squad;

import java.util.ArrayList;
import java.util.UUID;

public interface SquadHandler {
	/**
	 * Creates a new squad
	 * 
	 * @param id The owner
	 */
	public void createSquad(UUID id);

	/**
	 * Searches for user's squad, may be null
	 * 
	 * @param user The user
	 * @return The squad
	 */
	public Squad getSquad(UUID user);
	
	/**
	 * Returns all invites, a user has
	 * 
	 * @param user The user
	 * @return All invites
	 */
	public ArrayList<Squad> getInvitations(UUID user);
}
