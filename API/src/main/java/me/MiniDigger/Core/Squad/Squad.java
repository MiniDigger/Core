package me.MiniDigger.Core.Squad;

import java.util.ArrayList;
import java.util.UUID;

import me.MiniDigger.Core.Chat.ChatChannel;
import me.MiniDigger.Core.User.User;
import mkremins.fanciful.FancyMessage;

public interface Squad {
	
	/**
	 * @return the owner
	 */
	public UUID getOwner();
	
	/**
	 * @return the members
	 */
	public ArrayList<UUID> getMembers();
	
	/**
	 * @return the invites
	 */
	public ArrayList<UUID> getInvs();
	
	/**
	 * @return the channel
	 */
	public ChatChannel getChannel();
	
	/**
	 * @param channel
	 *            the channel to set
	 */
	public void setChannel(ChatChannel channel);
	
	/**
	 * Let the squad join a server
	 */
	public void joinServer();
	
	/**
	 * Let a user join a squad
	 * 
	 * @param user
	 *            The user
	 * @return If it was successful
	 */
	public boolean join(UUID user);
	
	/**
	 * Let a user leave the squad
	 * 
	 * @param user
	 *            The user
	 */
	public void leave(UUID user);
	
	/**
	 * Destroys a squad
	 */
	public void destroy();
	
	/**
	 * Sends a message to all squad members
	 * 
	 * @param msg
	 */
	public void sendMessage(FancyMessage msg);
	
	/**
	 * Let a user speak in the squad channel
	 * 
	 * @param user
	 *            The user
	 * @param message
	 *            The message
	 */
	public void chat(User user, String message);
	
	/**
	 * Invites a user
	 * 
	 * @param uuid
	 *            user's id
	 */
	public void invite(UUID uuid);
	
	/**
	 * Kicks a user
	 * 
	 * @param uuid
	 *            user's id
	 * @return If it was successful
	 */
	public boolean kick(UUID uuid);
}
