package me.MiniDigger.Core.Stats;

import java.util.UUID;

import me.MiniDigger.Core.SQL.Saveable;

public interface Stats extends Saveable {
	
	/**
	 * @return the user
	 */
	public UUID getUser();
	
	/**
	 * Sets the type to the value
	 * 
	 * @param type
	 * @param value
	 */
	public void set(StatsType type, int value);
	
	/**
	 * Addts the value to the current value
	 * 
	 * @param type
	 * @param value
	 */
	public void add(StatsType type, int value);
	
	/**
	 * Removes the value from the current value
	 * 
	 * @param type
	 * @param value
	 */
	public void remove(StatsType type, int value);
	
	/**
	 * Retuns the current value
	 * 
	 * @param type
	 * @return
	 */
	public int get(StatsType type);
	
	public void init();
}
