package me.MiniDigger.Core.Event.Events;

import me.MiniDigger.Core.Event.Event;
import me.MiniDigger.Core.Game.Game;
import me.MiniDigger.Core.User.User;

public interface UserDeathEvent extends Event {
	
	/**
	 * 
	 * @return The user, who died
	 */
	public User getUser();
	
	/**
	 * 
	 * @return The user, who killed the other. NULL if there is no killer
	 */
	public User getKiller();
	
	/**
	 * 
	 * @return The game, the death occured in
	 */
	public Game getGame();
	
	/**
	 * Sets if the should be deleted or not
	 * 
	 * @param keepDrops
	 *            false for deletion
	 */
	public void setKeepDrops(boolean keepDrops);
	
	/**
	 * 
	 * @return If the drops will be there or not
	 */
	public boolean keepDrops();
	
	/**
	 * Sets if the user should respawn or not
	 * 
	 * @param shouldRespawn
	 */
	public void setShouldRespawn(boolean shouldRespawn);
	
	/**
	 * 
	 * @return If the user will respawn or not
	 */
	public boolean shouldRespawn();
	
}
