package me.MiniDigger.Core.Command;

import java.lang.reflect.Method;
import java.util.List;

import org.bukkit.command.Command;
import org.bukkit.command.CommandSender;
import org.bukkit.command.TabCompleter;

public interface BukkitCompleter extends TabCompleter {
	
	/**
	 * Registers a new completer
	 * 
	 * @param label
	 *            The command lable the completer belongs to
	 * @param m
	 *            The method, the exetution code is in
	 * @param obj
	 *            The ibject the method is found in
	 */
	public void addCompleter(String label, Method m, Object obj);
	
	/**
	 * Requests a list of possible completions for a command argument.
	 * 
	 * @param sender
	 *            Source of the command
	 * @param command
	 *            Command which was executed
	 * @param alias
	 *            The alias used
	 * @param args
	 *            The arguments passed to the command, including final partial
	 *            argument to be completed and command label
	 * @return A List of possible completions for the final argument, or null to
	 *         default to the command executor
	 */
	public List<String> onTabComplete(CommandSender sender, Command command, String label, String[] args);
}
