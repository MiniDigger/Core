package me.MiniDigger.Core.AddOn.BedWars;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;

import org.bukkit.event.EventHandler;
import org.bukkit.event.EventPriority;
import org.bukkit.event.block.BlockBreakEvent;
import org.bukkit.event.block.BlockPlaceEvent;
import org.bukkit.event.player.PlayerRespawnEvent;

import me.MiniDigger.Core.Feature.FeatureType;
import me.MiniDigger.Core.Phase.Phase;
import me.MiniDigger.Core.Team.Team;

import me.MiniDigger.CraftCore.Feature.CoreFeature;
import me.MiniDigger.CraftCore.Feature.Features.TeamFeature;
import me.MiniDigger.CraftCore.Feature.Features.TeamSpawnFeature;

public class TeamBedFeature extends CoreFeature {
	
	private HashMap<String, BedFeature>	beds;
	private final int	                numTeams;
	
	public TeamBedFeature(final Phase phase, final int numTeams) {
		super(phase);
		this.numTeams = numTeams;
	}
	
	@Override
	public FeatureType getType() {
		return FeatureType.TEAM_BED;
	}
	
	@Override
	public List<FeatureType> getDependencies() {
		return new ArrayList<FeatureType>();
	}
	
	@Override
	public List<FeatureType> getSoftDependencies() {
		return new ArrayList<FeatureType>();
	}
	
	@Override
	public List<FeatureType> getIncompabilities() {
		return new ArrayList<FeatureType>();
	}
	
	@Override
	public void start() {
		beds = new HashMap<String, BedFeature>();
		
		final List<Team> teams = ((TeamFeature) getPhase().getFeature(FeatureType.TEAM)).getTeams();
		
		for (int i = 0; i < numTeams; i++) {
			final BedFeature f = new BedFeature(getPhase(), ((TeamSpawnFeature) getPhase().getFeature(FeatureType.TEAM_SPAWN)).getSpawn(teams.get(i)), teams.get(i)
			        .getName());
			f.init(getPhase());
			f.start();
			beds.put(teams.get(i).getName(), f);
		}
	}
	
	@Override
	public void end() {
		for (final BedFeature f : beds.values()) {
			f.end();
		}
	}
	
	@EventHandler
	public void onBedPlace(final BlockPlaceEvent e) {
		for (final BedFeature f : beds.values()) {
			f.onBlockPlayer(e);
		}
	}
	
	@EventHandler(priority = EventPriority.HIGH)
	public void onBedDestory(final BlockBreakEvent e) {
		for (final BedFeature f : beds.values()) {
			f.onBedDestory(e);
		}
	}
	
	@EventHandler(priority = EventPriority.NORMAL)
	public void onPlayerRespawn(final PlayerRespawnEvent e) {
		final TeamFeature tf = (TeamFeature) getPhase().getFeature(FeatureType.TEAM);
		beds.get(tf.getTeam(e.getPlayer()).getName()).onPlayerRespawn(e);
	}
}
