package me.MiniDigger.Core.Cinematic.meta;

import java.io.DataInputStream;
import java.io.DataOutputStream;
import java.io.IOException;

import me.MiniDigger.Core.Cinematic.Meta;
import me.MiniDigger.Core.Cinematic.MetaType;

/**
 * @author Mato Kormuth
 * 
 */
public class MetaEntityInventory implements Meta {
	
	private final long	internalId;
	private final byte	slot;
	private final int	itemType;
	
	/**
	 * @param internalId
	 * @param slot
	 * @param type
	 */
	public MetaEntityInventory(final long internalId, final byte slot, final int type) {
		super();
		this.internalId = internalId;
		this.slot = slot;
		itemType = type;
	}
	
	@Override
	public void writeMeta(final DataOutputStream stream) throws IOException {
		stream.writeLong(internalId);
		stream.writeByte(slot);
		stream.writeInt(itemType);
	}
	
	public static MetaEntityInventory readMeta(final DataInputStream stream) throws IOException {
		final long internalId = stream.readLong();
		final byte slot = stream.readByte();
		final int type = stream.readInt();
		
		return new MetaEntityInventory(internalId, slot, type);
	}
	
	@Override
	public int getType() {
		return 4;
	}
	
	@Override
	public MetaType getMetaType() {
		return MetaType.ENTITY_INVENTORY;
	}
	
	/**
	 * @return the internalId
	 */
	public long getInternalId() {
		return internalId;
	}
	
	/**
	 * @return the slot
	 */
	public byte getSlot() {
		return slot;
	}
	
	/**
	 * @return the itemType
	 */
	public int getItemType() {
		return itemType;
	}
}
