package me.MiniDigger.Core.Cinematic.meta;

import java.io.DataInputStream;
import java.io.DataOutputStream;
import java.io.IOException;

import me.MiniDigger.Core.Cinematic.Meta;
import me.MiniDigger.Core.Cinematic.MetaType;

/**
 * @author Mato Kormuth
 * 
 */
public class MetaEntityRemove implements Meta {
	
	private final long	internalId;
	
	/**
	 * @param internalId2
	 */
	public MetaEntityRemove(final long internalId2) {
		internalId = internalId2;
	}
	
	@Override
	public void writeMeta(final DataOutputStream stream) throws IOException {
		stream.writeLong(internalId);
	}
	
	public static MetaEntityRemove readMeta(final DataInputStream stream) throws IOException {
		final long internalId = stream.readLong();
		
		return new MetaEntityRemove(internalId);
	}
	
	@Override
	public MetaType getMetaType() {
		return MetaType.ENTITY_REMOVE;
	}
	
	/**
	 * @return the internalId
	 */
	public long getInternalId() {
		return internalId;
	}
	
}
