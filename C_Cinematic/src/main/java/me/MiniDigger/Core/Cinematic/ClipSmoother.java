package me.MiniDigger.Core.Cinematic;

import org.bukkit.Location;

/**
 * @author Mato Kormuth
 * 
 */
public class ClipSmoother {
	
	public static void smoothCompiledClip(final String path, final int a) throws Exception {
		CameraClip clip = CompiledReader.loadFile(path);
		
		clip = ClipSmoother.smooth(clip, a);
		
		final CompiledWriter writer = CompiledWriter.createFile(path);
		writer.writeClip(clip);
		writer.close();
	}
	
	public static CameraClip smoothClip(final CameraClip clip, final int a) {
		return ClipSmoother.smooth(clip, a);
	}
	
	/**
	 * @param clip
	 * @param i
	 * @return
	 */
	private static CameraClip smooth(final CameraClip clip, final int a) {
		final CameraClip newClip = new CameraClip();
		
		for (int i = 0; i < clip.frames.size(); i++) {
			final CameraFrame currentFrame = clip.frames.get(i);
			final CameraFrame nextFrame = clip.frames.get(i + 1);
			
			newClip.addFrame(currentFrame);
			
			// Smooth movement (X,Y,Z)
			final double x = 0;
			final double y = 0;
			final double z = 0;
			
			// Smooth rotation (Pitch, Yaw)
			final float newYaw = (currentFrame.getCameraLocation().getYaw() + nextFrame.getCameraLocation().getYaw()) / 2;
			final float newPitch = (currentFrame.getCameraLocation().getPitch() + nextFrame.getCameraLocation().getPitch()) / 2;
			
			final Location newLoc = new Location(null, x, y, z, newYaw, newPitch);
			final CameraFrame newFrame = new CameraFrame(newLoc, false);
			
			// Add new frame.
			newClip.addFrame(newFrame);
		}
		
		return newClip;
	}
}
