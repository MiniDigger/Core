package com.minnymin.zephyrus.core.util.map;

import java.util.Iterator;

/**
 * Zephyrus - MutliMapIterator.java
 *
 * @author minnymin3
 *
 * @param <A>
 *            Key type
 * @param <B>
 *            First value type
 * @param <C>
 *            Second value type
 */
public class MultiMapIterator<A, B, C> implements Iterator<MultiEntry<A, B, C>> {

	private MultiMap<A, B, C>	map			= null;
	private A					current		= null;
	private int					index		= 0;
	private int					lastIndex	= 0;

	public MultiMapIterator(final MultiMap<A, B, C> map) {
		this.map = map.clone();
	}

	@Override
	public boolean hasNext() {
		return index < map.size();
	}

	@Override
	public MultiEntry<A, B, C> next() {
		this.current = map.getKey(index);
		lastIndex = index;
		index++;
		return getCurrentEntry();
	}

	public MultiEntry<A, B, C> getCurrentEntry() {
		return this.map.getEntry(current);
	}

	@Override
	public void remove() {
		map.remove(lastIndex);
	}

}
