	public static void fix(final File file) throws Exception {
		if (file.isDirectory()) {
			for (final File f : file.listFiles()) {
				fix(f);
			}
			return;
		}
		
		boolean found = false;
		for (final String s : included) {
			if (file.getName().endsWith(s)) {
				found = true;
			}
		}
		
		if (!found) {
			System.out.println("File " + file.getAbsolutePath() + " is excluded!");
			excluded++;
			return;
		}
		
		final List<String> lines = new ArrayList<String>();
		final List<String> oldHeader = new ArrayList<String>();
		
		final BufferedReader reader = new BufferedReader(new FileReader(file));
		String line;
		found = false;
		while ((line = reader.readLine()) != null) {
			if (line.startsWith("package")) {
				found = true;
			}
			if (found) {
				lines.add(line);
			} else {
				oldHeader.add(line);
			}
		}
		reader.close();
		
		found = false;
		if (oldHeader.size() == 0) {
			added++;
			System.out.println("Adding header to " + file.getAbsolutePath() + "!");
			found = true;
		}
		
		for (int i = 0; i < oldHeader.size(); i++) {
			if (!oldHeader.get(i).equalsIgnoreCase(newHeader.get(i))) {
				found = true;
				break;
			}
		}
		
		if (!found) {
			System.out.println("Skipping " + file.getAbsolutePath() + "!");
			skipped++;
			return;
		}
		
		System.out.println("Updating file " + file.getAbsolutePath() + "!");
		changed++;
		
		final PrintWriter writer = new PrintWriter(file);
		
		for (final String s : newHeader) {
			writer.println(s);
		}
		
		for (final String s : lines) {
			writer.println(s);
		}
		
		writer.close();
	}
