package org.trypticon.megahal.engine;

import java.io.BufferedReader;
import java.io.FileReader;
import java.io.IOException;
import java.util.HashMap;
import java.util.HashSet;
import java.util.Iterator;
import java.util.List;
import java.util.Map;
import java.util.Set;

/**
 * Class for static utility methods.
 *
 * @author Trejkaz
 */
public class Utils {

	public static Map<Symbol, Symbol> readSymbolMapFromFile(final String filename) throws IOException {
		final Map<Symbol, Symbol> map = new HashMap<Symbol, Symbol>();

		final BufferedReader reader = new BufferedReader(new FileReader(filename));
		String line;
		while ((line = reader.readLine()) != null) {
			line = line.trim();
			if (line.length() == 0) {
				continue;
			}
			if (line.charAt(0) == '#') {
				continue;
			}
			final String[] words = line.split("\t");
			if (words.length != 2) {
				continue;
			}
			map.put(new Symbol(words[0], false), new Symbol(words[1], false));
		}
		reader.close();

		return map;
	}

	public static Set<Symbol> readSymbolSetFromFile(final String filename) throws IOException {
		final HashSet<Symbol> set = new HashSet<Symbol>();

		final BufferedReader reader = new BufferedReader(new FileReader(filename));
		String line;
		while ((line = reader.readLine()) != null) {
			line = line.trim();
			if (line.length() == 0) {
				continue;
			}
			if (line.charAt(0) == '#') {
				continue;
			}
			set.add(new Symbol(line, false));
		}
		reader.close();

		return set;
	}

	public static boolean equals(final List<?> l1, final List<?> l2) {
		if (l1.size() != l2.size()) {
			return false;
		}
		final Iterator<?> il1 = l1.iterator();
		final Iterator<?> il2 = l2.iterator();
		while (il1.hasNext()) {
			if (!il1.next().equals(il2.next())) {
				return false;
			}
		}
		return true;
	}
}
