package org.trypticon.megahal.engine;

import java.util.ArrayList;
import java.util.List;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

/**
 * A splitter which splits on word boundaries, splitting the string into a list
 * of 'word' fragments and 'non-word' fragments.
 *
 * @author Trejkaz
 */
public class WordNonwordSplitter implements Splitter {

	/**
	 * The regex pattern which defines word boundaries.
	 */
	private static Pattern boundaryPattern = Pattern.compile("([\\w']+|[^\\w']+)");

	/**
	 * Symbol factory for creating symbols.
	 */
	private final SymbolFactory symbolFactory;

	/**
	 * Creates the splitter.
	 *
	 * @param symbolFactory
	 *            symbol factory for creating symbols.
	 */
	public WordNonwordSplitter(final SymbolFactory symbolFactory) {
		this.symbolFactory = symbolFactory;
	}

	@Override
	public List<Symbol> split(final String text) {
		final List<Symbol> symbolList = new ArrayList<Symbol>();
		symbolList.add(Symbol.START);

		final Matcher m = boundaryPattern.matcher(text);
		while (m.find()) {
			final Symbol s = symbolFactory.createSymbol(m.group().toUpperCase().intern());
			symbolList.add(s);
		}
		symbolList.add(Symbol.END);
		return symbolList;
	}

	@Override
	public String join(List<Symbol> symbols) {
		// Chop off the <START> and <END>
		symbols = symbols.subList(1, symbols.size() - 1);

		// Build up the rejoined list.
		final StringBuffer result = new StringBuffer();
		for (final Symbol symbol : symbols) {
			result.append(symbol.toString().toLowerCase());
		}
		return result.toString();
	}
}
