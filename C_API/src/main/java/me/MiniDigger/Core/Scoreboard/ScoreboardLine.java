package me.MiniDigger.Core.Scoreboard;

import org.bukkit.scoreboard.DisplaySlot;

public interface ScoreboardLine {

	/**
	 * @param content
	 *            the new content
	 */
	void setContent(final String content);

	/**
	 * @return the content of this line
	 */
	String getContent();

	/**
	 * @param id
	 *            the new id
	 */
	void setId(final int id);

	/**
	 * @return the id of this line
	 */
	int getId();

	/**
	 * @return the slot the line is displayed
	 */
	DisplaySlot getSlot();

	/**
	 * @param slot
	 *            the slot the line should get displayed
	 */
	void setSlot(final DisplaySlot slot);

	/**
	 * @return if the line should scroll
	 */
	boolean scorlling();

	/**
	 * if scolling is activated this will scroll the text by one
	 */
	void scroll();

	/**
	 * @return the orig msg
	 */
	String getOrig();

}
