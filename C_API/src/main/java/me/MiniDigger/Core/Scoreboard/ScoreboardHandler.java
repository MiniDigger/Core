package me.MiniDigger.Core.Scoreboard;

import java.util.Set;
import java.util.UUID;

import org.bukkit.scoreboard.DisplaySlot;

public interface ScoreboardHandler {
	
	/**
	 * Gets the board for a player, create a new one if nothing was there
	 * 
	 * @param id
	 *            the id of the player
	 * @return the board for that player
	 */
	Scoreboard getBoard(final UUID id);
	
	/**
	 * Clears the sb of all players
	 */
	void clearAll();
	
	/**
	 * Reapplies the board for the user
	 * 
	 * @param id
	 */
	void update(final UUID id);
	
	/**
	 * Clears only one slot of the sb
	 * 
	 * @param slot
	 */
	void clearAll(final DisplaySlot slot);
	
	/**
	 * @return the ideas of all registered players
	 */
	Set<UUID> getIds();
	
	/**
	 * @param name
	 * @return the team of the player with this name
	 */
	ScoreboardTeam getTeam(final String name);
	
	/**
	 * updates the scoreboards of all online players
	 */
	void updateAll();
	
}
