package me.MiniDigger.Core.Team;

import java.util.List;
import java.util.UUID;

import org.bukkit.ChatColor;

import me.MiniDigger.Core.Game.Game;

public interface Team {
	
	/**
	 * @return the name
	 */
	public String getName();
	
	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(final String name);
	
	/**
	 * @return the game
	 */
	public Game getGame();
	
	/**
	 * @param game
	 *            the game to set
	 */
	public void setGame(final Game game);
	
	/**
	 * @return the size
	 */
	public int getSize();
	
	/**
	 * @param size
	 *            the size to set
	 */
	public void setSize(final int size);
	
	/**
	 * Returns the team data for the given key<br>
	 * may be null
	 * 
	 * @param key
	 *            the key
	 * @return the data
	 */
	public String getData(final String key);
	
	/**
	 * Saves team data<br>
	 * Overrides is existing
	 * 
	 * @param key
	 *            the key
	 * @param data
	 *            the data
	 */
	public void setData(final String key, final String data);
	
	/**
	 * @return the players
	 */
	public List<UUID> getPlayers();
	
	/**
	 * @param player
	 *            the player to join this team
	 */
	void join(final UUID player);
	
	/**
	 * @param player
	 *            the player to leave this team
	 */
	void leave(final UUID player);

	/**
     * @return the teams color
     */
    ChatColor getColor();

	/**
     * @param color the new color to set
     */
    void setColor(ChatColor color);
	
}
