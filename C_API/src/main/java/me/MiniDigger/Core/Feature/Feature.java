package me.MiniDigger.Core.Feature;

import java.util.List;

import org.bukkit.event.Listener;

import me.MiniDigger.Core.Phase.Phase;

public interface Feature extends Listener {

	/**
	 * @return The type of this feature
	 */
	public FeatureType getType();

	/**
	 * @return A list with all dependencies
	 */
	public List<FeatureType> getDependencies();

	/**
	 * @return A list with all soft-dependencies
	 */
	public List<FeatureType> getSoftDependencies();

	/**
	 * @return A list with all incomabilities
	 */
	public List<FeatureType> getIncompabilities();

	/**
	 * Inits this feature
	 *
	 * @param phase
	 *            The phase, this feature is running in
	 */
	public void init(final Phase phase);

	/**
	 * Starts the feature
	 */
	public void start();

	/**
	 * Stops the feature
	 */
	public void end();

	/**
	 * @return The phase, the feature is running in
	 */
	public Phase getPhase();
}
