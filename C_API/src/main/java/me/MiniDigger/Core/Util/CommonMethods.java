package me.MiniDigger.Core.Util;

import java.util.List;

import me.MiniDigger.Core.User.User;

import org.bukkit.command.CommandSender;

public interface CommonMethods {
	
	/**
	 * Stops the sever<br>
	 * <b>DEPRECATED: Use {@link ShutdownUtil} instead</b>
	 */
	@Deprecated()
	public void stopServer();
	
	/**
	 * Informs the consol, that this command can only be used ingame
	 * 
	 * @param sender
	 * @param command
	 */
	public void onlyPlayer(final CommandSender sender, final String command);
	
	/**
	 * Prints the join message for that user
	 * 
	 * @param user
	 *            the user to print the message
	 */
	public void printJoinMessage(final User user);
	
	/**
	 * Beendet das plugin
	 */
	void killPlugin();
	
	/**
	 * Tries to find the args, that match the arguments
	 * 
	 * @param list
	 *            all possible arguments
	 * @param prefix
	 *            the start of the argument
	 * @return the matched arguments
	 */
	public List<String> completer(final List<String> list, final String prefix);
}
