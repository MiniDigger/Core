package me.MiniDigger.Core.Lang;

public enum LangType {
	de_DE("de", "DE", "deutsch", "Deutschland"), en_US("en", "US", "english", "United States");
	
	private String	nameCode;
	private String	countryCode;
	private String	name;
	private String	country;
	
	private LangType(final String nameCode, final String countryCode, final String name, final String country) {
		this.nameCode = nameCode;
		this.countryCode = countryCode;
		this.name = name;
		this.country = country;
	}
	
	public String getNameCode() {
		return nameCode;
	}
	
	public String getCountryCode() {
		return countryCode;
	}
	
	public String getCode() {
		return nameCode + "_" + countryCode;
	}
	
	public String getName() {
		return name;
	}
	
	public String getCountry() {
		return country;
	}
}
