package me.MiniDigger.Core.Server;

import me.MiniDigger.Core.Game.GameType;

public interface Server {
	
	/**
	 * @return the primaryGameType
	 */
	public GameType getPrimaryGameType();
	
	/**
	 * @param primaryGameType
	 *            the primaryGameType to set
	 */
	public void setPrimaryGameType(GameType primaryGameType);
	
	/**
	 * @return the name
	 */
	public String getName();
	
	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name);
	
	/**
	 * @return the numPlayers
	 */
	public int getNumPlayers();
	
	/**
	 * @param numPlayers
	 *            the numPlayers to set
	 */
	public void setNumPlayers(int numPlayers);
	
	/**
	 * @return the maxPlayers
	 */
	public int getMaxPlayers();
	
	/**
	 * @param maxPlayers
	 *            the maxPlayers to set
	 */
	public void setMaxPlayers(int maxPlayers);
	
	/**
	 * @return the phase
	 */
	public String getPhase();
	
	/**
	 * @param phase
	 *            the phase to set
	 */
	public void setPhase(String phase);
	
	/**
	 * @return the join
	 */
	public boolean isJoin();
	
	/**
	 * @param join
	 *            the join to set
	 */
	public void setJoin(boolean join);
	
	/**
	 * @return the spectate
	 */
	public boolean isSpectate();
	
	/**
	 * @param spectate
	 *            the spectate to set
	 */
	public void setSpectate(boolean spectate);
	
	/**
	 * @return the online
	 */
	public boolean isOnline();
	
	/**
	 * @param online
	 *            the online to set
	 */
	public void setOnline(boolean online);
	
	/**
	 * Converts this bean into a string
	 * 
	 * @return
	 */
	public String toString();
	
	/**
	 * @return If the server is full
	 */
	public boolean isFull();
}
