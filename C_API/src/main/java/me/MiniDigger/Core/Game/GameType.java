package me.MiniDigger.Core.Game;

public enum GameType {
	
	TICTACTOE("TicTacToe", "TTT", null), LOBBY("Lobby", "Lobby", null), GETTHEDROP("GetTheDrop", "GTD", null), NOTHING("Nothing", "", null);
	
	private String	    name;
	private String	    abk;
	private Class<Game>	game;
	
	private GameType(final String name, final String abk, final Class<Game> game) {
		this.name = name;
		this.abk = abk;
		this.game = game;
	}
	
	public String getName() {
		return name;
	}
	
	public String getAbk() {
		return abk;
	}
	
	public Class<?> getGame() {
		return game;
	}
	
	@SuppressWarnings("unchecked")
	public void setClass(final Class<?> game) {
		this.game = (Class<Game>) game;
	}
}
