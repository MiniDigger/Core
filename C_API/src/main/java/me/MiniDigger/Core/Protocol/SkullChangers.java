package me.MiniDigger.Core.Protocol;

import java.util.List;
import java.util.UUID;

import com.comphenix.protocol.events.PacketEvent;

import org.bukkit.Location;
import org.bukkit.entity.Player;

public interface SkullChangers {
	
	/**
	 * Updates all the changer
	 * @param noUpdates a list with uuid not to update
	 */
	public void update(List<UUID> noUpdates);
	
	/**
	 * Adds all the changers
	 */
	public void init();
	
	/**
	 * Registers a signchanger
	 * 
	 * @param changer
	 *            The changer to register
	 */
	public void addSkullChanger(final SkullChanger changer);
	
	/**
	 * Unregister a changer
	 * 
	 * @param changer
	 *            The changer to unregister
	 */
	public void removeSkullChanger(final SkullChanger changer);
	
	/**
	 * @return All the registered changers
	 */
	public List<SkullChanger> getSkullChangerList();
	
	/**
	 * Handles the Skull packet
	 * 
	 * @param event
	 *            The Event
	 */
	public void handlePacket(final PacketEvent event);
	
	public abstract class SkullChanger {
		
		private final String	key;
		private final String	perm;
		private final String	desc;
		
		public SkullChanger(final String key, final String perm, final String desc) {
			this.key = key;
			this.perm = perm;
			this.desc = desc;
		}
		
		/**
		 * @return The key
		 */
		public String getKey() {
			return key;
		}
		
		/**
		 * @return The perm needed to create this type of changer
		 */
		public String getPerm() {
			return perm;
		}
		
		/**
		 * @return A basic description
		 */
		public String getDescription() {
			return desc;
		}
		
		/**
		 * Replaces the key with this value
		 * 
		 * @param p
		 *            The player, seeing the sign
		 * @param l
		 *            The location of the sign
		 * @return The value to replace the key
		 */
		public abstract String getValue(final Player player, final Location loc);
	}
}
