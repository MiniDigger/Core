package me.MiniDigger.Core.Error;

import me.MiniDigger.Core.Prefix.Prefix;
import me.MiniDigger.Core.User.User;

import org.bukkit.Bukkit;
import org.bukkit.ChatColor;
import org.bukkit.command.ConsoleCommandSender;

public enum Error {
	
	NO_ERROR("", "", "", ErrorType.CONSOL, ErrorLevel.MINOR),
	// Player
	USER_ALLREADY_JOINED("USER-1", "User allready joined", "User allready joined that game or channel!", ErrorType.BOTH, ErrorLevel.MINOR),
	USER_NOT_JOINED("USER-2", "User not joined", "The user is not in that game or channel!", ErrorType.BOTH, ErrorLevel.MINOR),
	// Consol
	// SQL
	SQL_CONNECTION_FAILED("SQL-1", "Connection failed", "Could not connect to the datebase!", ErrorType.CONSOL, ErrorLevel.BREAKING),
	SQL_STMT_CREATION_FAILED("SQL-2", "STMT creation failed", "Could not create a statement!", ErrorType.CONSOL, ErrorLevel.MAJOR),
	// MAP
	MAP_NO_SPAWN("MAP-1", "No spawn found", "Could not find a spawn!", ErrorType.BOTH, ErrorLevel.BREAKING);
	
	private String	   id;
	private String	   name;
	private String	   message;
	private ErrorType	type;
	private ErrorLevel	level;
	
	private Error(final String id, final String name, final String message, final ErrorType type, final ErrorLevel level) {
		this.id = id;
		this.name = name;
		this.message = message;
		this.type = type;
		this.level = level;
	}
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return the type
	 */
	public ErrorType getType() {
		return type;
	}
	
	/**
	 * @return the level
	 */
	public ErrorLevel getLevel() {
		return level;
	}
	
	/**
	 * Throws the error. Only applyable for consol errors
	 * 
	 * @param e
	 *            The exception, which created this error
	 */
	public void throwError(final Exception e) {
		if (type == ErrorType.CONSOL) {
			final ConsoleCommandSender c = Bukkit.getServer().getConsoleSender();
			c.sendMessage(Prefix.ERROR.getConsolPrefix() + ChatColor.RED + "Es ist ein Fehler ausgetreten:");
			c.sendMessage(Prefix.ERROR.getConsolPrefix() + ChatColor.RED + getName() + ": " + getMessage() + " (" + getId() + ")");
			c.sendMessage(Prefix.ERROR.getConsolPrefix() + ChatColor.RED + "Dieser Fehler ist " + getLevel().getVerb());
			c.sendMessage(Prefix.ERROR.getConsolPrefix() + ChatColor.RED + e.getMessage());
		}
	}
	
	/**
	 * Throws the error.
	 * 
	 * @param user
	 *            The user, who "caused" it
	 * @param ex
	 *            The exception, which created this error
	 */
	public void throwError(final User user, final Exception ex) {
		if (getType() == ErrorType.CONSOL) {
			throwError(ex);
		} else {
			user.sendMessage(Prefix.ERROR.getPrefix().then("Fehler: ").color(ChatColor.RED).then("(" + getLevel().name() + ")").color(ChatColor.DARK_RED)
			        .then(getName() + ": " + getMessage() + " ").color(ChatColor.RED).then("(" + getId() + ")").color(ChatColor.DARK_RED));
			
			if (getType() == ErrorType.BOTH) {
				throwError(ex);
			}
		}
	}
}
