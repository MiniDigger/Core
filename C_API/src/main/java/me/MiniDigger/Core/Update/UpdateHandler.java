package me.MiniDigger.Core.Update;

public interface UpdateHandler {
	
	public boolean updateCheck();
	
	public interface PluginVersion {
		/**
		 * 
		 * @return the raw version string
		 */
		public String getRaw();
		
		/**
		 * 
		 * @return the major #
		 */
		public int getMajor();
		
		/**
		 * 
		 * @return the minor #
		 */
		public int getMinor();
		
		/**
		 * 
		 * @return the commit #
		 */
		public int getCommit();
		
		/**
		 * 
		 * @return the build #
		 */
		public int getBuild();
		
		/**
		 * @param raw
		 *            the raw to set
		 */
		public void setRaw(String raw);
		
		/**
		 * @param major
		 *            the major to set
		 */
		public void setMajor(int major);
		
		/**
		 * @param minor
		 *            the minor to set
		 */
		public void setMinor(int minor);
		
		/**
		 * @param commit
		 *            the commit to set
		 */
		public void setCommit(int commit);
		
		/**
		 * @param build
		 *            the build to set
		 */
		public void setBuild(int build);
		
		/**
		 * Checks if the other version is newer for that type
		 * 
		 * @param other
		 * @param type
		 * @return
		 */
		public boolean isNewer(final PluginVersion other, final UpdateType type);
		
		/**
		 * Checks if the other version is newer or equal for that type
		 * 
		 * @param other
		 * @param type
		 * @return
		 */
		public boolean isNewerOrEqual(PluginVersion other, UpdateType type);
	}
}
