public enum ItemType {
	GRABLING_HOOK(null),
	GRAVITYGUN(null);

	private Item item;

	private ItemType(final Item item) {
		this.item = item;
	}

	/**
	 * NULL: if not installed!
	 *
	 * @return The item who is associated whith this type
	 */
	public Item getItem() {
		return item;
	}

	/**
	 * "Installs" the Item
	 *
	 * @param item
	 */
	public void setItem(final Item item) {
		this.item = item;
	}
}
