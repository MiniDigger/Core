package me.MiniDigger.Core.Command;

import me.MiniDigger.Core.User.User;

import org.bukkit.command.CommandSender;
import org.bukkit.entity.Player;

public interface CommandArgs {
	
	/**
	 * Gets the command sender
	 * 
	 * @return
	 */
	public CommandSender getSender();
	
	/**
	 * Gets the original command object
	 * 
	 * @return
	 */
	public org.bukkit.command.Command getCommand();
	
	/**
	 * Gets the label including sub command labels of this command
	 * 
	 * @return Something like 'test.subcommand'
	 */
	public String getLabel();
	
	/**
	 * Gets all the arguments after the command's label. ie. if the command
	 * label was test.subcommand and the arguments were subcommand foo foo, it
	 * would only return 'foo foo' because 'subcommand' is part of the command
	 * 
	 * @return
	 */
	public String[] getArgs();
	
	/**
	 * Checks if the sender was a player
	 * 
	 * @return
	 */
	public boolean isPlayer();
	
	/**
	 * Returns the player, who executed the command. If the command wasen't
	 * executed by a player it returns NULL
	 * 
	 * @return
	 */
	public Player getPlayer();
	
	/**
	 * Checks if the sender was a user
	 * 
	 * @return
	 */
	public boolean isUser();
	
	/**
	 * Returns the user, who executed the command. If the command wasen't
	 * executed by a user it returns NULL
	 * 
	 * @return
	 */
	public User getUser();
}
