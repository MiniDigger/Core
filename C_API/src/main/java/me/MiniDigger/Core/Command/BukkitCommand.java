package me.MiniDigger.Core.Command;

import java.util.List;

import org.bukkit.command.CommandException;
import org.bukkit.command.CommandSender;

public interface BukkitCommand {
	
	/**
	 * Executes the command, returning its success
	 * 
	 * @param sender
	 *            Source object which is executing this command
	 * @param commandLabel
	 *            The alias of the command used
	 * @param args
	 *            All arguments passed to the command, split via ' '
	 * @return true if the command was successful, otherwise false
	 */
	public boolean execute(CommandSender sender, String commandLabel, String[] args);
	
	/**
	 * Executed on tab completion for this command, returning a list of options
	 * the player can tab through.
	 * 
	 * @param sender
	 *            Source object which is executing this command
	 * @param alias
	 *            the alias being used
	 * @param args
	 *            All arguments passed to the command, split via ' '
	 * @return a list of tab-completions for the specified arguments. This will
	 *         never be null. List may be immutable.
	 * @throws IllegalArgumentException
	 *             if sender, alias, or args is null
	 */
	public List<String> tabComplete(CommandSender sender, String alias, String[] args) throws CommandException, IllegalArgumentException;
	
	/**
	 * @return the tab completer for this command
	 */
	public BukkitCompleter getCompleter();
	
	/**
	 * Sets the tab completer
	 * 
	 * @param c
	 *            The tab completer to set
	 */
	public void setCompleter(BukkitCompleter c);
}
