package me.MiniDigger.Core.AddOn;

import org.json.simple.JSONObject;

public interface AddOnBean {

	/**
	 * @return the name
	 */
	public String getName();

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(final String name);

	/**
	 * @return the author
	 */
	public String getAuthor();

	/**
	 * @param author
	 *            the author to set
	 */
	public void setAuthor(final String author);

	/**
	 * @return the version
	 */
	public String getVersion();

	/**
	 * @param version
	 *            the version to set
	 */
	public void setVersion(final String version);

	/**
	 *
	 * @return the object as json string
	 */
	public JSONObject toJson();

	/**
	 * @return the path to the main class in the jar
	 */
	public String getPackage();

	/**
	 * @param classPackage
	 *            sets the path to the main class in the jar
	 */
	public void setPackage(final String classPackage);

	/**
	 * @return the path to the main class in the jar (debug)
	 */
	public String getPackageDev();

	/**
	 * @param classPackage
	 *            sets the path to the main class in the jar (debug)
	 */
	public void setPackageDev(final String packageDev);
}
