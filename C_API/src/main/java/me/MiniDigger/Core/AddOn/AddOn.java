package me.MiniDigger.Core.AddOn;

import java.io.File;

import org.bukkit.configuration.file.FileConfiguration;

public interface AddOn {
	
	/**
	 * @returns the name of this addon
	 */
	public String getName();
	
	/**
	 * Gets called when the addon gets enabled
	 */
	public void enable();
	
	/**
	 * Gets called when the addon gets disabled
	 */
	public void disable();
	
	/**
	 * @returns The dataFolder of this addon, where files should be placed
	 */
	public File getDataFolder();
	
	/**
	 * @returns The config for this addon
	 */
	public FileConfiguration getConfig();
	
	/**
	 * Loads the addon.
	 * 
	 * @param bean
	 *            some informations about the addon
	 */
	public void load(final AddOnBean bean);
	
	/**
	 * Saves the config to the disk
	 */
	public void saveConfig();
	
	/**
	 * Loads the config back from disk
	 */
	public void loadConfig();
	
	/**
	 * @return the bean with some informations about the addon
	 */
	public AddOnBean getBean();
	
	/**
	 * Checks if there is a newer version of this addon on the main repo
	 */
	public void checkUpdate();
}
