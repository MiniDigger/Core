import me.MiniDigger.CraftCore.Event.Events.CoreUserJoinGameEvent;
import me.MiniDigger.CraftCore.Feature.CoreFeature;

public class JoinHandlerFeature extends CoreFeature {
	
	public JoinHandlerFeature(final Phase phase) {
		super(phase);
	}
	
	@Override
	public FeatureType getType() {
		return FeatureType.JOINHANDLER;
	}
	
	@Override
	public List<FeatureType> getDependencies() {
		return new ArrayList<FeatureType>();
	}
	
	@Override
	public List<FeatureType> getSoftDependencies() {
		return new ArrayList<FeatureType>();
	}
	
	@Override
	public List<FeatureType> getIncompabilities() {
		return new ArrayList<FeatureType>();
	}
	
	@Override
	public void start() {
		// if (getPhase().getGame().getPlayers().size() <= 1) {
		// final User u =
		// Core.getCore().getUserHandler().get(getPhase().getGame().getPlayers().get(0));
		// getPhase().getGame().end(u);
		// }
	}
	
	@Override
	public void end() {
	
	}
	
	@EventHandler
	public void onJoin(final CoreUserJoinGameEvent e) {
		if (getPhase().getGame().getIdentifier() == e.getGame().getIdentifier()) {
			if (getPhase().getGame().allowJoin()) {
				Core.getCore().getPlayerUtil().prepare(e.getUser().getPlayer());
				try {
					Location loc = ((MapFeature) getPhase().getFeature(FeatureType.MAP)).getMap().getLocs(DyeColor.RED).get(0);
					if (loc != null) {
						e.getUser().getPlayer().teleport(loc);
					}
				} catch (final Exception ex) {
					Core.getCore().getInstance().debug("error while join tp");
				}
			} else if (getPhase().getGame().allowSpectate()) {
				Core.getCore().getPlayerUtil().prepare(e.getUser().getPlayer());
				try {
					Core.getCore().getInstance().debug("join as spec");
					((SpecateFeature) getPhase().getFeature(FeatureType.SPEC)).spec(e.getUser());
				} catch (Exception ex) {
					Core.getCore().getInstance().debug("could not specate");
				}
			} else {
				Core.getCore().getInstance().debug("fuck you, you can't join here: " + getPhase().getGame().getType().name());
			}
		}
	}
}
