package me.MiniDigger.CraftCore.Scoreboard;

import org.bukkit.scoreboard.DisplaySlot;

import me.MiniDigger.Core.Scoreboard.ScoreboardLine;

public class CoreScoreboardLine implements ScoreboardLine {
	
	private int	        id;
	private String	    content;
	private DisplaySlot	slot;
	private boolean	    scrolling;
	private String	    orig;
	
	public CoreScoreboardLine(final int id, final String content, final DisplaySlot slot) {
		this.id = id;
		this.content = content;
		this.slot = slot;
		if (content.length() > 14) {
			scrolling = true;
			orig = content;
		}
	}
	
	@Override
	public String getOrig() {
		return orig;
	}
	
	@Override
	public void scroll() {
		// TODO Scoll
		getContent().replaceAll(getContent(), "newcontent");
	}
	
	@Override
	public boolean scorlling() {
		return scrolling;
	}
	
	@Override
	public int getId() {
		return id;
	}
	
	@Override
	public void setId(final int id) {
		this.id = id;
	}
	
	@Override
	public String getContent() {
		return content;
	}
	
	@Override
	public void setContent(final String content) {
		this.content = content;
	}
	
	@Override
	public DisplaySlot getSlot() {
		return slot;
	}
	
	@Override
	public void setSlot(final DisplaySlot slot) {
		this.slot = slot;
	}
}
