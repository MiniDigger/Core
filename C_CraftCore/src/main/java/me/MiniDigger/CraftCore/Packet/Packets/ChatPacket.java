package me.MiniDigger.CraftCore.Packet.Packets;

import java.util.UUID;
import java.util.regex.Pattern;

import org.java_websocket.WebSocket;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Chat.ChatChannel;

import me.MiniDigger.CraftCore.Packet.CorePacket;
import me.MiniDigger.CraftCore.Server.CoreServer;

public class ChatPacket extends CorePacket {
	
	private UUID	user;
	private String	server;
	private String	msg;
	
	private String	channel;
	
	@Override
	public String toString() {
		return getName() + "|" + user.toString() + "|" + server + "|" + channel + "|" + msg;
	}
	
	@Override
	public void fromString(final String s) {
		final String[] ss = s.split(Pattern.quote("|"));
		
		user = UUID.fromString(ss[1]);
		server = ss[2];
		channel = ss[3];
		msg = ss[4];
	}
	
	@Override
	public void handle() {
		if (CoreServer.getForThis(true).getName().equalsIgnoreCase(server)) {
			return;
		}
		try {
			final ChatChannel c = Core.getCore().getChatHandler().getChannel(channel);
			if (c.isGlobal()) {
				c.chat(Core.getCore().getUserHandler().get(user), server + "|" + msg);
			}
		} catch (final Exception ex) {
			System.out.println("No channel called " + channel + " at this server!");
		}
	}
	
	@Override
	public void handle(final WebSocket con) {
		Core.getCore().getPacketHandler().sendBroadcast(this);
	}
	
	@Override
	public String getName() {
		return "Chat";
	}
	
	public UUID getUser() {
		return user;
	}
	
	public void setUser(final UUID u) {
		user = u;
	}
	
	public String getServer() {
		return server;
	}
	
	public void setServer(final String s) {
		server = s;
	}
	
	public String getMessage() {
		return msg;
	}
	
	public void setMessage(final String msg) {
		this.msg = msg;
	}
	
	public String getChannel() {
		return channel;
	}
	
	public void setChannel(final String channel) {
		this.channel = channel;
	}
}
