package me.MiniDigger.CraftCore.Packet.Packets;

import java.util.regex.Pattern;

import org.bukkit.Bukkit;
import org.java_websocket.WebSocket;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Socket.Session;
import me.MiniDigger.CraftCore.Packet.CorePacket;

public class CommandPacket extends CorePacket {

	private String	toServer;
	private String	cmd;

	@Override
	public String toString() {
		return getName() + "|" + toServer + "|" + cmd;
	}

	@Override
	public void fromString(final String s) {
		final String[] ss = s.split(Pattern.quote("|"));

		toServer = ss[1];
		cmd = ss[2];
	}

	@Override
	public void handle() {
		Bukkit.dispatchCommand(Bukkit.getConsoleSender(), cmd);
	}

	@Override
	public void handle(final WebSocket con) {
		if (toServer.equalsIgnoreCase("all")) {
			Core.getCore().getPacketHandler().sendBroadcast(this);
		} else {
			final Session s = Core.getCore().getSocketHandler().getSession(toServer);
			if (s == null) {
				Core.getCore().getInstance().debug("no client called " + toServer);
				return;
			}
			Core.getCore().getPacketHandler().sendPacket(this, s);
		}
	}

	@Override
	public String getName() {
		return "Command";
	}
}
