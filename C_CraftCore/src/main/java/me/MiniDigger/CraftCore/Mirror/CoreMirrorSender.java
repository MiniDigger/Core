package me.MiniDigger.CraftCore.Mirror;

import java.io.IOException;
import java.io.ObjectOutputStream;
import java.net.ServerSocket;
import java.net.Socket;
import java.util.ArrayList;
import java.util.List;

import net.minecraft.util.com.google.gson.Gson;

import org.json.simple.JSONObject;
import org.json.simple.parser.JSONParser;

import me.MiniDigger.Core.Mirror.MirrorSender;

public class CoreMirrorSender implements MirrorSender {
	
	private ServerSocket	   socket;
	private List<ClientThread>	clients	= new ArrayList<>();
	
	@Override
	public void init(int port) {
		new ServerThread(port).start();
	}
	
	@Override
	public void send(Object event) {
		for (ClientThread thread : clients) {
			thread.send(event);
		}
	}
	
	private class ServerThread extends Thread {
		
		private int	port;
		
		public ServerThread(int port) {
			setName("MirrorServerThread");
			this.port = port;
		}
		
		@Override
		public void run() {
			try {
				System.out.println("server started");
				socket = new ServerSocket(port);
				Socket client;
				while ((client = socket.accept()) != null) {
					System.out.println("client connected");
					ClientThread thread = new ClientThread(client);
					thread.start();
					clients.add(thread);
				}
				socket.close();
			} catch (Exception ex) {
				ex.printStackTrace();
			}
		}
	}
	
	private class ClientThread extends Thread {
		
		// TODO is this cool?
		private Socket	client;
		
		/**
		 * @param accept
		 */
		public ClientThread(Socket accept) {
			setName("MirrorClientThread");
			client = accept;
		}
		
		/**
		 * @param event
		 */
		public void send(Object event) {
			try {
				ObjectOutputStream out = new ObjectOutputStream(client.getOutputStream());
				out.writeObject(new Gson().toJson(event));
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
		
	}
}
