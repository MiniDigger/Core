package me.MiniDigger.CraftCore.Achievement;

import me.MiniDigger.Core.Achievement.AchievmentLevel;

public class CoreAchievmentLevel implements AchievmentLevel {

	private int	id;
	private int	data;
	private int	points;

	public CoreAchievmentLevel(final int id, final int data, final int points) {
		this.id = id;
		this.data = data;
		this.points = points;
	}

	@Override
	public int getId() {
		return id;
	}

	@Override
	public void setId(final int id) {
		this.id = id;
	}

	@Override
	public int getData() {
		return data;
	}

	@Override
	public void setData(final int data) {
		this.data = data;
	}

	@Override
	public int getPoints() {
		return points;
	}

	@Override
	public void setPoints(final int points) {
		this.points = points;
	}

}
