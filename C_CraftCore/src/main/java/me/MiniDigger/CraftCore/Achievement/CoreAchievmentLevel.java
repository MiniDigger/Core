package me.MiniDigger.CraftCore.Achievement;

import me.MiniDigger.Core.Achievement.AchievmentLevel;

public class CoreAchievmentLevel implements AchievmentLevel {
	
	private int	id;
	private int	data;
	private int	points;
	
	public CoreAchievmentLevel(int id, int data, int points) {
		this.id = id;
		this.data = data;
		this.points = points;
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getData() {
		return data;
	}
	
	public void setData(int data) {
		this.data = data;
	}
	
	public int getPoints() {
		return points;
	}
	
	public void setPoints(int points) {
		this.points = points;
	}
	
}
