package me.MiniDigger.CraftCore.Error;

import java.util.Enumeration;
import java.util.logging.Handler;
import java.util.logging.Level;
import java.util.logging.LogManager;
import java.util.logging.LogRecord;
import java.util.logging.Logger;

import me.MiniDigger.Core.Error.ErrorHandler;
import me.MiniDigger.Core.Lang.LangKeyType;
import me.MiniDigger.Core.Lang.LogLevel;
import me.MiniDigger.CraftCore.Lang.MSG;

public class CoreErrorHandler implements ErrorHandler {

	private Handler h;

	@Override
	public void init() {
		h = new Handler() {

			@Override
			public void flush() {
			}

			@Override
			public void close() throws SecurityException {
			}

			@Override
			public void publish(final LogRecord record) {
				if (record == null) {
					return;
				}
				if (record.getThrown() == null) {
					return;
				}
				if (record.getLevel() == Level.SEVERE) {
					String msg = record.getThrown().getMessage();
					if (msg == null) {
						msg = record.getThrown().getCause().getMessage();
						if (msg == null) {
							msg = "<msg null>";
						}
					}
					MSG.log(LogLevel.ERROR, LangKeyType.Log.CATCHED, "3", msg);
					MSG.stacktrace(LogLevel.DEBUG, record.getThrown());
					MSG.log(LogLevel.DEBUG, LangKeyType.Log.CAUSED);
					MSG.stacktrace(LogLevel.DEBUG, record.getThrown().getCause());
				}
			}

		};

		final Enumeration<String> e = LogManager.getLogManager().getLoggerNames();
		while (e.hasMoreElements()) {
			final String s = e.nextElement();
			final Logger l = LogManager.getLogManager().getLogger(s);
			l.addHandler(h);
		}
	}
}
