package me.MiniDigger.CraftCore.Regen;

import java.util.List;

import org.bukkit.Bukkit;
import org.bukkit.block.BlockState;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Regen.Regen;
import me.MiniDigger.CraftCore.CoreMain;

public class CoreRegen implements Regen {
	
	private String	         name;
	private List<BlockState>	blocks;
	
	@Override
	public String getName() {
		return name;
	}
	
	@Override
	public void reset(boolean fancy, int delay) {
		int i = 1;
		if (fancy) {
			while (!blocks.isEmpty()) {
				final BlockState state = blocks.get(Core.getCore().getRandomUtil().nextInt(blocks.size()));
				Bukkit.getScheduler().runTaskLater((CoreMain) Core.getCore().getInstance(), new Runnable() {
					
					@Override
					public void run() {
						state.update(true);
					}
				}, delay * i);
				i++;
				blocks.remove(state);
			}
		} else {
			for (final BlockState block : blocks) {
				Bukkit.getScheduler().runTaskLater((CoreMain) Core.getCore().getInstance(), new Runnable() {
					
					@Override
					public void run() {
						block.update(true);
					}
				}, delay * i);
				i++;
			}
		}
	}
	
}
