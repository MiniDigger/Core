package me.MiniDigger.CraftCore.Dependencies;

import me.MiniDigger.Core.Dependencies.Dependency;

public class CoreDependency implements Dependency {

	private String	name;
	private String	version;

	public CoreDependency(final String name, final String version) {
		this.name = name;
		this.version = version;
	}

	@Override
	public String getName() {
		return name;
	}

	@Override
	public void setName(final String name) {
		this.name = name;
	}

	@Override
	public String getVersion() {
		return version;
	}

	@Override
	public void setVersion(final String version) {
		this.version = version;
	}

	@Override
	public String getFullName() {
		return getName() + "-" + getVersion();
	}
}
