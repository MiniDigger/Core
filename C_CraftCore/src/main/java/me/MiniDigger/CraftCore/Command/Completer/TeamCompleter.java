package me.MiniDigger.CraftCore.Command.Completer;

import java.util.ArrayList;
import java.util.List;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Command.CommandArgs;
import me.MiniDigger.Core.Command.Completer;
import me.MiniDigger.Core.Feature.FeatureType;
import me.MiniDigger.Core.Team.Team;
import me.MiniDigger.CraftCore.Feature.Features.TeamSelectFeature;

public class TeamCompleter {

	@Completer(name = "team")
	public List<String> addOnC(final CommandArgs args) {
		final List<String> result = new ArrayList<>();

		if (args.getArgs().length == 1) {
			result.add("join");
			result.add("list");

			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}

	@Completer(name = "team.join")
	public List<String> joinC(final CommandArgs args) {
		final TeamSelectFeature f = (TeamSelectFeature) Core.getCore().getGameHandler().getMainGame().getPhase().getFeature(FeatureType.TEAM_SELECT);
		if (f == null) {
			return new ArrayList<String>();
		}

		if (args.getArgs().length == 1) {
			final List<String> names = new ArrayList<String>();
			for (final Team teams : f.getTeams()) {
				names.add(teams.getName());
			}

			return Core.getCore().getCommonMethods().completer(names, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}

	@Completer(name = "team.list")
	public List<String> listC(final CommandArgs args) {
		return new ArrayList<String>();
	}
}
