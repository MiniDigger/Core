package me.MiniDigger.CraftCore.Command;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Command.CommandArgs;
import me.MiniDigger.Core.User.User;

import org.bukkit.command.CommandSender;
import org.bukkit.entity.Player;

public class CoreCommandArgs implements CommandArgs {
	
	private final CommandSender	             sender;
	private final org.bukkit.command.Command	command;
	private final String	                 label;
	private final String[]	                 args;
	private User	                         user;
	
	protected CoreCommandArgs(final CommandSender sender, final org.bukkit.command.Command command, final String label, final String[] args, final int subCommand) {
		final String[] modArgs = new String[args.length - subCommand];
		for (int i = 0; i < args.length - subCommand; i++) {
			modArgs[i] = args[i + subCommand];
		}
		
		final StringBuffer buffer = new StringBuffer();
		buffer.append(label);
		for (int x = 0; x < subCommand; x++) {
			buffer.append("." + args[x]);
		}
		final String cmdLabel = buffer.toString();
		this.sender = sender;
		this.command = command;
		this.label = cmdLabel;
		this.args = modArgs;
		
		/* Core Start */
		if (isPlayer()) {
			user = Core.getCore().getUserHandler().get(getPlayer().getUniqueId());
		}
		/* Core End */
	}
	
	@Override
	public CommandSender getSender() {
		return sender;
	}
	
	@Override
	public org.bukkit.command.Command getCommand() {
		return command;
	}
	
	@Override
	public String getLabel() {
		return label;
	}
	
	@Override
	public String[] getArgs() {
		return args;
	}
	
	@Override
	public boolean isPlayer() {
		return sender instanceof Player;
	}
	
	@Override
	public Player getPlayer() {
		if (sender instanceof Player) {
			return (Player) sender;
		} else {
			return null;
		}
	}
	
	/* Core Start */
	@Override
	public boolean isUser() {
		return user != null;
	}
	
	@Override
	public User getUser() {
		return user;
	}
	/* Core End */
}
