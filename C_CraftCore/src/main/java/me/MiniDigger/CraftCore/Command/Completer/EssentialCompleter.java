package me.MiniDigger.CraftCore.Command.Completer;

import java.util.ArrayList;
import java.util.List;

import org.bukkit.BanEntry;
import org.bukkit.BanList.Type;
import org.bukkit.Bukkit;
import org.bukkit.entity.EntityType;
import org.bukkit.entity.Player;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Command.CommandArgs;
import me.MiniDigger.Core.Command.Completer;

/**
 * @author Martin
 * 
 */
public class EssentialCompleter {
	
	@Completer(name = "spawnmob")
	public List<String> spawnmobC(final CommandArgs args) {
		final List<String> result = new ArrayList<String>();
		
		if (args.getArgs().length == 1) {
			
			for (final EntityType t : EntityType.values()) {
				result.add(t.name());
			}
			
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		}
		
		return new ArrayList<String>();
	}
	
	@Completer(name = "ban")
	public List<String> banC(final CommandArgs args) {
		if (args.getArgs().length == 1) {
			final List<String> result = new ArrayList<>();
			for (final Player p : Core.getCore().getUserHandler().getOnlinePlayers()) {
				result.add(p.getName());
			}
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}
	
	@Completer(name = "tempban")
	public List<String> tempbanC(final CommandArgs args) {
		if (args.getArgs().length == 1) {
			final List<String> result = new ArrayList<>();
			for (final Player p : Core.getCore().getUserHandler().getOnlinePlayers()) {
				result.add(p.getName());
			}
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}
	
	@Completer(name = "unban")
	public List<String> unbanC(final CommandArgs args) {
		if (args.getArgs().length == 1) {
			final List<String> result = new ArrayList<>();
			for (BanEntry e : Bukkit.getBanList(Type.NAME).getBanEntries()) {
				result.add(e.getTarget());
			}
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}
}
