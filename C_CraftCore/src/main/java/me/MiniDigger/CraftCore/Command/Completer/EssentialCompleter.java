package me.MiniDigger.CraftCore.Command.Completer;

import java.util.ArrayList;
import java.util.List;

import org.bukkit.entity.EntityType;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Command.CommandArgs;
import me.MiniDigger.Core.Command.Completer;

/**
 * @author Martin
 * 
 */
public class EssentialCompleter {
	
	@Completer(name = "spawnmob")
	public List<String> spawnmobC(final CommandArgs args) {
		final List<String> result = new ArrayList<String>();
		
		if (args.getArgs().length == 1) {
			
			for (final EntityType t : EntityType.values()) {
				result.add(t.name());
			}
			
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		}
		
		return new ArrayList<String>();
	}
}
