package me.MiniDigger.CraftCore.Command.Completer;

import java.util.ArrayList;
import java.util.List;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Command.CommandArgs;
import me.MiniDigger.Core.Command.Completer;
import me.MiniDigger.Core.Game.GameType;

public class GameCompleter {
	
	@Completer(name = "game")
	public List<String> gameC(final CommandArgs args) {
		final List<String> result = new ArrayList<>();
		
		if (args.getArgs().length == 1) {
			result.add("join");
			result.add("leave");
			result.add("info");
			result.add("list");
			
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}
	
	@Completer(name = "game.join")
	public List<String> joinC(final CommandArgs args) {
		final List<String> result = new ArrayList<>();
		
		if (args.getArgs().length == 1) {
			for (final GameType t : GameType.values()) {
				if (t != GameType.LOBBY && t != GameType.NOTHING && t != GameType.TICTACTOE) {
					result.add(t.name());
				}
			}
			
			return Core.getCore().getCommonMethods().completer(result, args.getArgs()[0]);
		} else {
			return new ArrayList<String>();
		}
	}
	
	@Completer(name = "game.leave")
	public List<String> leaveC(final CommandArgs args) {
		return new ArrayList<String>();
	}
	
	@Completer(name = "game.info")
	public List<String> infoC(final CommandArgs args) {
		return new ArrayList<String>();
	}
	
	@Completer(name = "game.list")
	public List<String> listC(final CommandArgs args) {
		return new ArrayList<String>();
	}
}
