package me.MiniDigger.CraftCore.Socket.Client;

import java.util.ArrayList;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Socket.Client.Client;
import me.MiniDigger.Core.Socket.Client.ClientHandler;

public class CoreClientHandler implements ClientHandler {

	private final ArrayList<Client> clients = new ArrayList<>();

	@Override
	public boolean loadAll() {
		Core.getCore().getInstance().info("Erstelle Tabelle für Externe User...");
		if (!new CoreClient(null).createTable()) {
			Core.getCore().getInstance().error("Tabelle wurde nicht erstellt!");
		}
		return true;
	}

	@Override
	public boolean saveAll() {
		return true;
	}

	@Override
	public Client getClient(final String name) {
		for (final Client client : clients) {
			if (client.getName().equals(name)) {
				return client;
			}
		}
		final Client user = new CoreClient(name);
		user.load();
		clients.add(user);
		return user;
	}

}
