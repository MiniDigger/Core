package me.MiniDigger.CraftCore.Socket;

import java.net.InetSocketAddress;

import me.MiniDigger.Core.Socket.Session;
import me.MiniDigger.Core.Socket.Client.Client;

public class CoreSession implements Session {
	
	private String	          name;
	private InetSocketAddress	address;
	private Client	          client;
	private boolean	          identified	= false;
	
	public CoreSession(final InetSocketAddress address) {
		this.address = address;
	}
	
	@Override
	public String getName() {
		return name;
	}
	
	@Override
	public Client getClient() {
		return client;
	}
	
	@Override
	public boolean isIdentified() {
		return identified;
	}
	
	@Override
	public void setName(final String name) {
		this.name = name;
	}
	
	@Override
	public void setAddress(final InetSocketAddress address) {
		this.address = address;
	}
	
	@Override
	public void setClient(final Client client) {
		this.client = client;
	}
	
	@Override
	public void setIdentified(final boolean identified) {
		this.identified = identified;
	}
	
	@Override
	public InetSocketAddress getAddress() {
		return address;
	}
	
}
