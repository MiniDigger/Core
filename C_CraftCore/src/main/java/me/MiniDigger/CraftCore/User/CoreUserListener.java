package me.MiniDigger.CraftCore.User;

import org.bukkit.event.player.PlayerJoinEvent;
import org.bukkit.event.player.PlayerQuitEvent;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.User.User;
import me.MiniDigger.Core.User.UserListener;

public class CoreUserListener implements UserListener {
	
	@Override
	public void onPlayerJoin(PlayerJoinEvent e) {
		User user = Core.getCore().getUserHandler().get(e.getPlayer().getUniqueId());
		user.startSession();
		user.getStats();
		Core.getCore().getCommonMethods().printJoinMessage(user);
	}
	
	@Override
	public void onPlayerQuit(PlayerQuitEvent e) {
		User user = Core.getCore().getUserHandler().get(e.getPlayer().getUniqueId());
		user.stopSession();
	}
	
}
