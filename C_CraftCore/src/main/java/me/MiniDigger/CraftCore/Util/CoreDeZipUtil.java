package me.MiniDigger.CraftCore.Util;

import java.io.BufferedInputStream;
import java.io.BufferedOutputStream;
import java.io.File;
import java.io.FileInputStream;
import java.io.FileOutputStream;
import java.io.IOException;
import java.util.zip.ZipEntry;
import java.util.zip.ZipInputStream;

import me.MiniDigger.Core.Util.DeZipUtil;

public class CoreDeZipUtil implements DeZipUtil {
	
	public void UnzipToDirectory(String zipFilePath, String outputDirectory) {
		FileInputStream fileInputStream = null;
		ZipInputStream zipInputStream = null;
		FileOutputStream fileOutputStream = null;
		BufferedOutputStream bufferedOutputStream = null;
		BufferedInputStream bufferedInputStream = null;
		
		try {
			fileInputStream = new FileInputStream(zipFilePath);
			bufferedInputStream = new BufferedInputStream(fileInputStream);
			zipInputStream = new ZipInputStream(bufferedInputStream);
			
			ZipEntry entry;
			while ((entry = zipInputStream.getNextEntry()) != null) {
				byte[] buffer = new byte[2048];
				
				File file = new File(outputDirectory + File.separator + entry.getName());
				file.mkdirs();
				
				fileOutputStream = new FileOutputStream(outputDirectory + File.separator + entry.getName());
				bufferedOutputStream = new BufferedOutputStream(fileOutputStream, buffer.length);
				int size;
				while ((size = zipInputStream.read(buffer, 0, buffer.length)) != -1) {
					bufferedOutputStream.write(buffer, 0, size);
				}
				
				bufferedOutputStream.flush();
				bufferedOutputStream.close();
				fileOutputStream.flush();
				fileOutputStream.close();
			}
			
			zipInputStream.close();
			bufferedInputStream.close();
			fileInputStream.close();
		} catch (IOException e) {
			e.printStackTrace();
			
			if (fileInputStream != null) {
				try {
					fileInputStream.close();
				} catch (IOException e1) {
					e1.printStackTrace();
				}
			}
			
			if (bufferedInputStream != null) {
				try {
					bufferedInputStream.close();
				} catch (IOException e1) {
					e1.printStackTrace();
				}
			}
			
			if (zipInputStream != null) {
				try {
					zipInputStream.close();
				} catch (IOException e1) {
					e1.printStackTrace();
				}
			}
			
			if (fileOutputStream != null) {
				try {
					fileOutputStream.close();
				} catch (IOException e1) {
					e1.printStackTrace();
				}
			}
			
			if (bufferedOutputStream != null) {
				try {
					bufferedOutputStream.close();
				} catch (IOException e1) {
					e1.printStackTrace();
				}
			}
		}
	}
	
	private final int	BUFFER_SIZE	= 4096;
	
	private void extractFile(ZipInputStream in, File outdir, String name) throws IOException {
		byte[] buffer = new byte[BUFFER_SIZE];
		File f = new File(outdir, name);
		f.getParentFile().mkdirs();
		File p = f.getParentFile();
		p.setWritable(true);
		try {
			f.createNewFile();
		} catch (Exception ex) {
			System.out.println("failed");
		}
		BufferedOutputStream out = new BufferedOutputStream(new FileOutputStream(f));
		int count = -1;
		while ((count = in.read(buffer)) != -1)
			out.write(buffer, 0, count);
		out.close();
	}
	
	private void mkdirs(File outdir, String path) {
		File d = new File(outdir, path);
		if (!d.exists()) d.mkdirs();
	}
	
	private String dirpart(String name) {
		int s = name.lastIndexOf(File.separatorChar);
		return s == -1 ? null : name.substring(0, s);
	}
	
	public void extract(File zipfile, File outdir) {
		try {
			ZipInputStream zin = new ZipInputStream(new FileInputStream(zipfile));
			ZipEntry entry;
			String name, dir;
			while ((entry = zin.getNextEntry()) != null) {
				name = entry.getName();
				if (entry.isDirectory()) {
					mkdirs(outdir, name);
					continue;
				}
				dir = dirpart(name);
				if (dir != null) mkdirs(outdir, dir);
				try {
					extractFile(zin, outdir, name);
				} catch (Exception ex) {
					// ex.printStackTrace();
					System.out.println("Error: " + ex.getMessage());
					ex.printStackTrace();
				}
			}
			zin.close();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
	
}
