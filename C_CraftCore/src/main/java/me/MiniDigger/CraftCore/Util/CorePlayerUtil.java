package me.MiniDigger.CraftCore.Util;

import java.util.ArrayList;
import java.util.List;

import me.MiniDigger.Core.Util.PlayerUtil;

import org.bukkit.Location;
import org.bukkit.Material;
import org.bukkit.block.Block;
import org.bukkit.entity.Entity;
import org.bukkit.entity.LivingEntity;
import org.bukkit.entity.Player;
import org.bukkit.inventory.ItemStack;
import org.bukkit.util.BlockIterator;

public class CorePlayerUtil implements PlayerUtil {
	
	@Override
	public Entity getTarget(final Player p, final int range) {
		final List<Entity> nearbyE = p.getNearbyEntities(range, range, range);
		final ArrayList<LivingEntity> livingE = new ArrayList<LivingEntity>();
		
		for (final Entity e : nearbyE) {
			if (e instanceof LivingEntity) {
				livingE.add((LivingEntity) e);
			}
		}
		
		final BlockIterator bItr = new BlockIterator(p, range);
		Block block;
		Location loc;
		int bx, by, bz;
		double ex, ey, ez;
		// loop through player's line of sight
		while (bItr.hasNext()) {
			block = bItr.next();
			bx = block.getX();
			by = block.getY();
			bz = block.getZ();
			// check for entities near this block in the line of sight
			for (final LivingEntity e : livingE) {
				loc = e.getLocation();
				ex = loc.getX();
				ey = loc.getY();
				ez = loc.getZ();
				if ((bx - .75 <= ex && ex <= bx + 1.75) && (bz - .75 <= ez && ez <= bz + 1.75) && (by - 1 <= ey && ey <= by + 2.5)) {
					// entity is close enough, set target and stop
					return e;
				}
			}
		}
		return null;
	}
	
	@Override
	public void clearArmor(final Player p) {
		final ItemStack[] items = p.getInventory().getArmorContents();
		for (int i = 0; i < items.length; i++) {
			items[i] = new ItemStack(Material.AIR);
		}
		p.getInventory().setArmorContents(items);
	}
	
	@Override
	public void clearInv(final Player p) {
		final ItemStack[] items = p.getInventory().getContents();
		for (int i = 0; i < items.length; i++) {
			items[i] = new ItemStack(Material.AIR);
		}
		p.getInventory().setContents(items);
	}
	
	@Override
	public void fixHealth(final Player p) {
		p.setHealth(20d);
		p.setSaturation(1);
		p.setFoodLevel(20);
		p.getActivePotionEffects().clear();
	}
	
	@Override
	public void prepare(final Player p) {
		clearArmor(p);
		clearInv(p);
		fixHealth(p);
	}
	
}
