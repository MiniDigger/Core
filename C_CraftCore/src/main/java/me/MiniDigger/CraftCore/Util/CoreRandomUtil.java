package me.MiniDigger.CraftCore.Util;

import java.util.Random;

import me.MiniDigger.Core.Util.RandomUtil;

public class CoreRandomUtil implements RandomUtil {

	private final Random r = new Random();

	@Override
	public void setSeed(final long seed) {
		r.setSeed(seed);
	}

	@Override
	public void nextBytes(final byte[] bytes) {
		r.nextBytes(bytes);
	}

	@Override
	public int nextInt() {
		return r.nextInt();
	}

	@Override
	public int nextInt(final int n) {
		return r.nextInt(n);
	}

	@Override
	public long nextLong() {
		return r.nextLong();
	}

	@Override
	public boolean nextBoolean() {
		return r.nextBoolean();
	}

	@Override
	public float nextFloat() {
		return r.nextFloat();
	}

	@Override
	public double nextDouble() {
		return r.nextDouble();
	}

	@Override
	public double nextGaussian() {
		return r.nextGaussian();
	}

	@Override
	public String nextString() {
		return nextString(nextInt(10));
	}

	@Override
	public String nextString(final int l) {
		final String s = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz1234567890<>,;.:-_öäü*+'#`´?ß~}][ {=)(/&%$§!°";
		String result = "";

		for (int i = 0; i < l; i++) {
			result += s.charAt(nextInt(s.length()));
		}

		return result;
	}
}
