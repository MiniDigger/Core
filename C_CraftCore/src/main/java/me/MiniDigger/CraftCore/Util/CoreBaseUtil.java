package me.MiniDigger.CraftCore.Util;

import org.apache.commons.codec.binary.Base64;

import me.MiniDigger.Core.Util.BaseUtil;

public class CoreBaseUtil implements BaseUtil {

	@Override
	public String encode(final String s) {
		final byte[] b = org.apache.commons.codec.binary.Base64.encodeBase64(s.getBytes());
		return new String(b);
	}

	@Override
	public String decode(final String s) {
		final byte[] b = Base64.decodeBase64(s.getBytes());
		return (new String(b));
	}
}
