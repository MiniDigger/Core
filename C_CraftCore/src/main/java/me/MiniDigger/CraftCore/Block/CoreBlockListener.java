package me.MiniDigger.CraftCore.Block;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Block.BlockListener;
import me.MiniDigger.Core.User.User;

import org.bukkit.event.EventHandler;
import org.bukkit.event.block.BlockBreakEvent;
import org.bukkit.event.block.BlockDamageEvent;
import org.bukkit.event.block.BlockPlaceEvent;

public class CoreBlockListener implements BlockListener {
	
	@Override
	@EventHandler
	public void onBlockBreak(final BlockBreakEvent e) {
		final User user = Core.getCore().getUserHandler().get(e.getPlayer().getUniqueId());
		if (!Core.getCore().getBuildHandler().isBuilder(user) || !Core.getCore().getBuildHandler().allow(user, e.getBlock())) {
			e.setCancelled(true);
		}
	}
	
	@Override
	@EventHandler
	public void onBlockPlace(final BlockPlaceEvent e) {
		final User user = Core.getCore().getUserHandler().get(e.getPlayer().getUniqueId());
		if (!Core.getCore().getBuildHandler().isBuilder(user) || !Core.getCore().getBuildHandler().allow(user, e.getBlock())) {
			e.setCancelled(true);
		}
	}
	
	@Override
	@EventHandler
	public void onBlockDamage(final BlockDamageEvent e) {
		final User user = Core.getCore().getUserHandler().get(e.getPlayer().getUniqueId());
		if (!Core.getCore().getBuildHandler().isBuilder(user) || !Core.getCore().getBuildHandler().allow(user, e.getBlock())) {
			e.setCancelled(true);
		}
	}
	
}
