package me.MiniDigger.CraftCore.Villager;

import org.bukkit.craftbukkit.v1_8_R3.inventory.CraftItemStack;
import org.bukkit.inventory.ItemStack;

import me.MiniDigger.Core.Villager.VillagerTrade;

public class CoreVillagerTrade implements VillagerTrade {

	private final ItemStack	item1;
	private ItemStack		item2;
	private final ItemStack	rewardItem;

	public CoreVillagerTrade(final ItemStack item1, final ItemStack item2, final ItemStack rewardItem) {
		this.item1 = item1;
		this.item2 = item2;
		this.rewardItem = rewardItem;
	}

	public CoreVillagerTrade(final ItemStack item1, final ItemStack rewardItem) {
		this.item1 = item1;
		this.rewardItem = rewardItem;
	}

	public CoreVillagerTrade(final net.minecraft.server.v1_8_R3.ItemStack buyItem1, final net.minecraft.server.v1_8_R3.ItemStack buyItem2, final net.minecraft.server.v1_8_R3.ItemStack buyItem3) {
		item1 = CraftItemStack.asCraftMirror(buyItem1);
		item2 = CraftItemStack.asCraftMirror(buyItem2);
		rewardItem = CraftItemStack.asCraftMirror(buyItem3);
	}

	@Override
	public boolean hasItem2() {
		return item2 != null;
	}

	@Override
	public ItemStack getItem1() {
		return item1;
	}

	@Override
	public ItemStack getItem2() {
		return item2;
	}

	@Override
	public ItemStack getRewardItem() {
		return rewardItem;
	}
}
