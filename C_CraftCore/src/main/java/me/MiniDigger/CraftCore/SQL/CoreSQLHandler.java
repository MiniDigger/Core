package me.MiniDigger.CraftCore.SQL;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.SQL.SQLHandler;
import me.MiniDigger.CraftCore.CoreMain;

public class CoreSQLHandler implements SQLHandler {
	
	private String	user;
	private String	pass;
	private String	host;
	private String	port;
	private String	db;
	private String	url;
	
	public CoreSQLHandler() {
		getData();
		generateURL();
	}
	
	private void getData() {
		user = get("sql-user");
		pass = get("sql-pass");
		host = get("sql-host");
		port = get("sql-port");
		db = get("sql-db");
	}
	
	private String get(String s) {
		return ((CoreMain)Core.getCore().getInstance()).getConfig().getString(s);
	}
	
	private void generateURL() {
		url = "jdbc:mysql://" + host + ":" + port + "/" + db;
	}
	
	@Override
	public String getUser() {
		return user;
	}
	
	@Override
	public String getPass() {
		return pass;
	}
	
	@Override
	public String getHost() {
		return host;
	}
	
	@Override
	public String getPort() {
		return port;
	}
	
	@Override
	public String getDB() {
		return db;
	}
	
	@Override
	public String getUrl() {
		return url;
	}
	
}
