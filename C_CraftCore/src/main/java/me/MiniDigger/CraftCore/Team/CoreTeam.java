package me.MiniDigger.CraftCore.Team;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.UUID;

import me.MiniDigger.Core.Game.Game;
import me.MiniDigger.Core.Team.Team;

public class CoreTeam implements Team {
	
	private String	                  name;
	private Game	                  game;
	private int	                      size;
	private final List<UUID>	      players	= new ArrayList<UUID>();
	private final Map<String, String>	data	= new HashMap<>();
	
	public CoreTeam(final int teamSize) {
		size = teamSize;
	}
	
	@Override
	public String getName() {
		return name;
	}
	
	@Override
	public void setName(final String name) {
		this.name = name;
	}
	
	@Override
	public Game getGame() {
		return game;
	}
	
	@Override
	public void setGame(final Game game) {
		this.game = game;
	}
	
	@Override
	public int getSize() {
		return size;
	}
	
	@Override
	public void setSize(final int size) {
		this.size = size;
	}
	
	@Override
	public String getData(final String key) {
		return data.get(key);
	}
	
	@Override
	public void setData(final String key, final String data) {
		if (this.data.containsKey(key)) {
			this.data.remove(key);
		}
		this.data.put(key, data);
	}
	
	@Override
	public List<UUID> getPlayers() {
		return players;
	}
	
	@Override
	public void join(final UUID player) {
		if (!players.contains(player)) {
			players.add(player);
		}
	}
	
	@Override
	public void leave(final UUID player) {
		players.remove(player);
	}
}
