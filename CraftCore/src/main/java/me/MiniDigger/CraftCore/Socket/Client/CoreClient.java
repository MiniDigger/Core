package me.MiniDigger.CraftCore.Socket.Client;

import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;

import me.MiniDigger.Core.SQL.SQLQuery;
import me.MiniDigger.Core.Socket.Client.Client;
import me.MiniDigger.CraftCore.SQL.CoreSQLQuery;

public class CoreClient implements Client {
	
	private String	name;
	private String	password;
	
	public CoreClient(String name) {
		this.name = name;
		password = "";
	}
	
	@Override
	public boolean save() {
		// Try insertion
		try {
			SQLQuery query = new CoreSQLQuery("INSERT INTO `external_users`(`name`, `password`) VALUES (?,?)");
			PreparedStatement stmt = query.getStatement();
			stmt.setString(1, name);
			stmt.setString(2, password);
			
			stmt.execute();
			query.kill();
		} catch (Exception ex) {
			// Try update
			try {
				SQLQuery query = new CoreSQLQuery("UPDATE `external_users` SET `name`=?,`password`=? WHERE `name` LIKE ?");
				PreparedStatement stmt = query.getStatement();
				stmt.setString(1, name);
				stmt.setString(2, password);
				
				stmt.setString(9, name);
				
				stmt.execute();
				query.kill();
			} catch (Exception e) {
				return false;
			}
		}
		return true;
	}
	
	@Override
	public boolean load() {
		try {
			SQLQuery query = new CoreSQLQuery("SELECT * FROM `external_users` WHERE `name` LIKE ?");
			PreparedStatement stmt = query.getStatement();
			stmt.setString(1, name);
			
			ResultSet r = stmt.executeQuery();
			if (r == null) {
				throw new NullPointerException("ResultSet returned by query can not be null!");
			}
			
			r.next();
			name = r.getString("name");
			password = r.getString("password");
			query.kill();
		} catch (Exception ex) {
			return false;
		}
		return true;
	}
	
	@Override
	public boolean createTable() {
		SQLQuery q = new CoreSQLQuery(
		        "CREATE TABLE IF NOT EXISTS `external_users` (`name` varchar(40) NOT NULL, `password` varchar(40) NOT NULL, PRIMARY KEY (`name`), UNIQUE KEY `name` (`name`))");
		try {
			q.getStatement().execute();
			q.kill();
			return true;
		} catch (SQLException e) {
			e.printStackTrace();
			return false;
		}
	}
	
	@Override
	public String getName() {
		return name;
	}
	
	@Override
	public String getPassword() {
		return password;
	}
	
	@Override
	public void setName(String name) {
		this.name = name;
	}
	
	@Override
	public void setPassword(String password) {
		this.password = password;
	}
	
}
