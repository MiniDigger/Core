package me.MiniDigger.CraftCore.NPC;

import org.bukkit.Bukkit;
import org.bukkit.ChatColor;
import org.bukkit.Location;
import org.bukkit.plugin.Plugin;

import com.lenis0012.bukkit.npc.NPC;
import com.lenis0012.bukkit.npc.NPCAnimation;
import com.lenis0012.bukkit.npc.NPCFactory;
import com.lenis0012.bukkit.npc.NPCProfile;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.NPC.NPCHandler;

public class CoreNPCHandler implements NPCHandler {
	
	private final NPCFactory	factory	= new NPCFactory((Plugin) Core.getCore().getInstance());
	private NPC	             bender;
	
	@Override
	public NPCFactory getFactory() {
		return factory;
	}
	
	@Override
	public NPC getBender() {
		return bender;
	}
	
	@Override
	public void spawn(final Location loc) {
		final NPCProfile profile = new NPCProfile(ChatColor.GOLD + "Bender", "bender12");
		Bukkit.getScheduler().runTask((Plugin) Core.getCore().getInstance(), new Runnable() {
			
			@Override
			public void run() {
				bender = factory.spawnHumanNPC(loc, profile);
				bender.setYaw(loc.getYaw());
				bender.getBukkitEntity().teleport(loc);
				bender.setEntityCollision(false);
				bender.setGravity(false);
				bender.playAnimation(NPCAnimation.SWING_ARM);
			}
		});
	}
	
}
