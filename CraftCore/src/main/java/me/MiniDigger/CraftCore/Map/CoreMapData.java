package me.MiniDigger.CraftCore.Map;

import java.io.File;
import java.io.IOException;
import java.util.ArrayList;
import java.util.HashMap;

import org.bukkit.Bukkit;
import org.bukkit.DyeColor;
import org.bukkit.Location;
import org.bukkit.Material;
import org.bukkit.World;
import org.bukkit.configuration.file.FileConfiguration;
import org.bukkit.configuration.file.YamlConfiguration;
import org.bukkit.material.Wool;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Map.MapData;

public class CoreMapData implements MapData {
	
	private String	                                     name;
	private HashMap<DyeColor, HashMap<String, Location>>	locs;
	
	public CoreMapData(String name) {
		this.name = name;
		locs = new HashMap<>();
	}
	
	public CoreMapData(FileConfiguration con) {
		load(con);
	}
	
	@Override
	public String getName() {
		return name;
	}
	
	@Override
	public HashMap<String, Location> getLocs(DyeColor type) {
		return locs.get(type);
	}
	
	@Override
	public void createConfig() {
		File file = new File(Bukkit.getWorld(name).getWorldFolder(), "map.yml");
		try {
			file.createNewFile();
		} catch (IOException e1) {
			e1.printStackTrace();
		}
		FileConfiguration config = YamlConfiguration.loadConfiguration(file);
		
		config.set("name", name);
		
		ArrayList<String> types = new ArrayList<>();
		for (DyeColor type : locs.keySet()) {
			types.add(type.name());
		}
		config.set("locTypes", types);
		
		for (DyeColor type : locs.keySet()) {
			ArrayList<String> keys = new ArrayList<>();
			for (String key : locs.get(type).keySet()) {
				Location loc = locs.get(type).get(key);
				String d = key + ";" + loc.getX() + ";" + loc.getY() + ";" + loc.getZ() + ";" + loc.getPitch() + ";" + loc.getYaw();
				keys.add(d);
			}
			config.set(type.name(), keys);
		}
		
		try {
			config.save(file);
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
	
	@Override
	public void load(FileConfiguration con) {
		name = con.getString("name");
		
		locs = new HashMap<>();
		
		ArrayList<DyeColor> types = new ArrayList<>();
		for (String s : con.getStringList("locTypes")) {
			DyeColor t;
			try {
				t = DyeColor.valueOf(s);
			} catch (Exception ex) {
				Core.getCore().getInstance().error("Could not read LocType " + s + "!");
				continue;
			}
			types.add(t);
		}
		
		for (DyeColor t : types) {
			HashMap<String, Location> locs = new HashMap<>();
			for (String s : con.getStringList(t.name())) {
				try {
					String[] split = s.split(";");
					
					String key = split[0];
					double x = Double.parseDouble(split[1]);
					double y = Double.parseDouble(split[2]);
					double z = Double.parseDouble(split[3]);
					float pitch = Float.parseFloat(split[4]);
					float yaw = Float.parseFloat(split[5]);
					World w = Bukkit.getWorld(name);
					Location loc = new Location(w, x, y, z, pitch, yaw);
					
					locs.put(key, loc);
				} catch (Exception ex) {
					Core.getCore().getInstance().error("Could not read Location " + t.name() + "." + s + "!");
					continue;
				}
			}
			this.locs.put(t, locs);
		}
	}
	
	@Override
	public void scanMap(final Location start, final int range, final Runnable finished) {
		Thread thread = new Thread(new Runnable() {
			
			@SuppressWarnings("deprecation")
			@Override
			public void run() {
				HashMap<DyeColor, ArrayList<Location>> locs = new HashMap<>();
				
				int startX = start.getBlockX();
				int startY = start.getBlockZ();
				
				int minX = startX - range;
				int minY = 0;
				int minZ = startY - range;
				
				int maxX = startX + range;
				int maxY = start.getWorld().getMaxHeight();
				int maxZ = startY + range;
				
				for (int x = minX; x <= maxX; x++) {
					for (int y = minY; y <= maxY; y++) {
						for (int z = minZ; z <= maxZ; z++) {
							Location loc = new Location(start.getWorld(), x, y, z);
							if (loc.getBlock().getType() == Material.WOOL) {
								DyeColor color = DyeColor.getByWoolData(loc.getBlock().getData());
								ArrayList<Location> wLocs = locs.get(color);
								if (color == DyeColor.BLACK) {
									continue;// skip black, often used for back
									         // of windows
								}
								if (wLocs == null) {
									wLocs = new ArrayList<>();
								}
								wLocs.add(loc);
								
								locs.remove(color);
								locs.put(color, wLocs);
								// TODO We will need some kind of pitch and ywa
								// for the spawns
							}
						}
					}
				}
				for (DyeColor color : locs.keySet()) {
					ArrayList<Location> loc = locs.get(color);
					HashMap<String, Location> l = new HashMap<>();
					for (int i = 0; i < loc.size(); i++) {
						l.put(i + "", loc.get(i));
					}
					CoreMapData.this.locs.put(color, l);
				}
				finished.run();
			}
		});
		
		thread.setName("MapScanner #1");
		thread.start();
	}
	
	@Override
	public int clearLocs() {
		int i = 0;
		for (HashMap<String, Location> map : locs.values()) {
			for (Location l : map.values()) {
				try {
					if (l == null || l.getBlock() == null) {
						continue;
					}
				} catch (Exception ex) {
					continue;
				}
				l.getBlock().setType(Material.AIR);
				i++;
			}
		}
		return i;
	}
	
	@SuppressWarnings("deprecation")
	@Override
	public int setLocs() {
		int i = 0;
		for (DyeColor color : locs.keySet()) {
			HashMap<String, Location> map = locs.get(color);
			for (Location l : map.values()) {
				l.setWorld(Bukkit.getWorld(name));
				try {
					if (l == null || l.getBlock() == null) {
						System.out.println("Failed i");
						continue;
					}
				} catch (Exception ex) {
					System.out.println("Failed !");
					// ex.printStackTrace(); Always NPE at
					// org.bukkit.Location.getBlock(Location.java:82)
					continue;
				}
				Wool wool = new Wool(color);
				l.getBlock().setType(Material.WOOL);
				l.getBlock().setData(wool.getData());
				i++;
			}
		}
		return i;
	}
	
	@Override
	public int loadChunks() {
		int i = 0;
		for (HashMap<String, Location> map : locs.values()) {
			for (Location l : map.values()) {
				l.setWorld(Bukkit.getWorld(name));
				if (!l.getChunk().isLoaded() || l.getWorld().isChunkLoaded(l.getChunk())) {
					l.getChunk().load();
					l.getWorld().loadChunk(l.getChunk());
					i++;
				}
			}
		}
		return i;
	}
	
}
