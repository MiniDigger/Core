package me.MiniDigger.CraftCore.Bar;

import java.lang.reflect.Field;
import java.lang.reflect.Method;
import java.util.HashMap;
import java.util.Map;
import java.util.UUID;

import me.MiniDigger.Core.Core;
import me.MiniDigger.Core.Bar.BarHandler;
import me.MiniDigger.Core.Bar.FakeDragon;

import org.bukkit.Bukkit;
import org.bukkit.entity.Player;

public class CoreBarHandler implements BarHandler {
	
	private Map<UUID, FakeDragon>	DRAGONS	= new HashMap<>();
	
	public boolean hasBar(Player player) {
		return DRAGONS.containsKey(player.getUniqueId()) && DRAGONS.get(player) != null;
	}
	
	public void removeBar(Player player) {
		if (hasBar(player)) {
			sendPacket(player, DRAGONS.get(player).getDestroyPacket());
			DRAGONS.remove(player);
		}
	}
	
	public void setBar(Player player, String text, float percent) {
		FakeDragon dragon = DRAGONS.containsKey(player.getUniqueId()) ? DRAGONS.get(player.getUniqueId()) : null;
		
		if (text.length() > 64) text = text.substring(0, 63);
		if (percent > 1.0f) percent = 1.0f;
		if (percent < 0.05f) percent = 0.05f;
		
		if (text.isEmpty() && dragon != null) removeBar(player);
		
		if (dragon == null) {
			dragon = new CoreFakeDragon(player.getLocation().add(0, -200, 0), text, percent);
			sendPacket(player, dragon.getSpawnPacket());
			DRAGONS.put(player.getUniqueId(), dragon);
		} else {
			dragon.setName(text);
			dragon.setHealth(percent);
			sendPacket(player, dragon.getMetaPacket(dragon.getWatcher()));
			sendPacket(player, dragon.getTeleportPacket(player.getLocation().add(0, -200, 0)));
		}
		
	}
	
	public void setBar(Player player, String text, double health) {
		FakeDragon dragon = DRAGONS.containsKey(player.getUniqueId()) ? DRAGONS.get(player.getUniqueId()) : null;
		
		if (text.length() > 64) text = text.substring(0, 63);
		if (health > 200) health = 200;
		if (health < 0) health = 0;
		
		if (text.isEmpty() && dragon != null) removeBar(player);
		
		if (dragon == null) {
			dragon = new CoreFakeDragon(player.getLocation().add(0, -200, 0), text, (float) health);
			sendPacket(player, dragon.getSpawnPacket());
			DRAGONS.put(player.getUniqueId(), dragon);
		} else {
			dragon.setName(text);
			dragon.setHealth(health);
			sendPacket(player, dragon.getMetaPacket(dragon.getWatcher()));
			sendPacket(player, dragon.getTeleportPacket(player.getLocation().add(0, -200, 0)));
		}
	}
	
	public void removeAllStatusBars() {
		for (Player each : Bukkit.getOnlinePlayers()) {
			removeBar(each);
		}
	}
	
	public void setAllStatusBars(String text, float percent) {
		for (Player each : Bukkit.getOnlinePlayers()) {
			setBar(each, text, percent);
		}
	}
	
	private static void sendPacket(Player player, Object packet) {
		try {
			Object nmsPlayer = Core.getCore().getReflectionUtil().getHandle(player);
			Field connectionField = nmsPlayer.getClass().getField("playerConnection");
			Object connection = connectionField.get(nmsPlayer);
			Method sendPacket = Core.getCore().getReflectionUtil().getMethod(connection.getClass(), "sendPacket");
			sendPacket.invoke(connection, packet);
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
}
